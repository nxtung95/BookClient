    public void setProductList(List<ProductDto> products) {
        try {
            Object[] columnName = {"STT", "Mã SP", "Loại SP", "Tên SP", "Số lượng", "Đơn giá", "Nhà cung cấp", "Hình ảnh", "Tác giả", "Số trang", "Nhà xuất bản"};
            Object[][] listObj = new Object[500][500];
            int index = 0;
            int i = 0;
            for (ProductDto p : products) {
                BufferedImage img = ImageIO.read(new File(p.getImage()));
                ImageIcon icon = new ImageIcon(img);
                Image imgRe = icon.getImage();
                BufferedImage resizedImg = new BufferedImage(100, 100, BufferedImage.TYPE_INT_ARGB);
                Graphics2D g2 = resizedImg.createGraphics();

                g2.setRenderingHint(RenderingHints.KEY_INTERPOLATION, RenderingHints.VALUE_INTERPOLATION_BILINEAR);
                g2.drawImage(imgRe, 0, 0, 100, 100, null);
                g2.dispose();
                Object[] obj = {
                    i + 1,
                    p.getProductId(),
                    p.getCategoryName(),
                    p.getProductName(),
                    10,
                    p.getPrice(),
                    p.getSupplier(),
                    new ImageIcon(resizedImg),
                    p.getAuthor(),
                    p.getTotalPage(),
                    p.getPublisher()
                };
                listObj[index++] = obj;
                i++;
            }
            DefaultTableModel model = new DefaultTableModel(listObj, columnName) {
                @Override
                public Class getColumnClass(int column) {
                    if (column == 7) {
                        return ImageIcon.class;
                    }
                    return Object.class;
                }
            };
            jTable1.setModel(model);
            jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
            jTable1.getColumn(columnName[0]).setPreferredWidth(20);
            jTable1.getColumn(columnName[1]).setPreferredWidth(50);
            jTable1.getColumn(columnName[2]).setPreferredWidth(50);
            jTable1.getColumn(columnName[3]).setPreferredWidth(125);
            jTable1.getColumn(columnName[4]).setPreferredWidth(50);
            jTable1.getColumn(columnName[5]).setPreferredWidth(150);
            jTable1.getColumn(columnName[6]).setPreferredWidth(100);
            jTable1.getColumn(columnName[7]).setPreferredWidth(215);
            jTable1.getColumn(columnName[8]).setPreferredWidth(100);
            jTable1.getColumn(columnName[9]).setPreferredWidth(50);
            jTable1.getColumn(columnName[10]).setPreferredWidth(75);
            jTable1.setRowHeight(150);
        } catch(Exception e) {
            e.printStackTrace();
        }
    }

    public void setCategoryList(List<CategoryDto> categories) {
        jComboBox1.addItem("Tất cả");
        for (CategoryDto c : categories) {
            jComboBox1.addItem(c.getName());
        }
        this.categoryList = categories;
    }

