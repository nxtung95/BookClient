    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String productId = jTextField1.getText();
        String productName = jTextField2.getText();
        String productType = (String) jComboBox1.getSelectedItem();
        Optional<CategoryDto> opt = this.categoryList.stream().filter(c -> productType.equals(c.getName())).findFirst();
        int categoryId = 0;
        if (opt.isPresent()) {
            categoryId = opt.get().getId();
        }
        String supplier = jTextField3.getText();
        ProductController productControl = new ProductController();
        List<ProductDto> productList = productControl.search(productId, productName, categoryId, supplier);
        setProductList(productList);
    }//GEN-LAST:event_jButton1ActionPerformed

